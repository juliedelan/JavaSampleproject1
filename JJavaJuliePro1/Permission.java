package JJavaJuliePro1;

// Permission object class
public class Permission {
	  
	  public String role;
	  public String name;
	  public boolean active;
	  
	// empty class constructor.You can create an empty object
	public  Permission(){}
	  
	// Permission class constructor : if create permission object, that will have these lists    
	  public Permission(String role, String name, boolean active) {
	    this.role = role;
	    this.name = name;
	    this.active = active;
	  }

	  
	  
	}// end Permission class



	